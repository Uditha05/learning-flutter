class MathFunction {
  String doMultiply(firstNum, secondNum) {
    int result = firstNum * secondNum;
    return result.toString();
  }

  String doAdd(firstNum, secondNum) {
    int result = firstNum + secondNum;
    return result.toString();
  }

  String doDivide(firstNum, secondNum) {
    int result = firstNum / secondNum;
    return result.toString();
  }

  String doSubtract(firstNum, secondNum) {
    int result = firstNum - secondNum;
    return result.toString();
  }
}
